class Alphabet 
{
    public static void main(String[] args) 
    {
        System.out.println("Capital Letter");
        for(char i='A';i<='Z';i++)
        {
            System.out.print(i+" ");
        }
        System.out.println();
        System.out.println("Small Letter");
        for(char j='a';j<='z';j++)
        {
            System.out.print(j+" ");
        }
    }
}
